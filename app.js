let a = Number(prompt("Enter number"));
let operator = prompt("Enter an operator (+, -, *, /):");
let b = Number(prompt("Enter number"));
if (operator == "+"){
    alert(a+b)
}else if(operator == "-"){
    alert(a-b)
}else if(operator == "/"){
    alert(a-b)
}else if(operator == "/"){
    alert(a-b)
}else if(operator == "*"){
    alert(a*b)
}else{
    alert("invalid")
}